package practice;

public class SwitchBreakDemo {

	public static void main(String[] args) {
     int day=2;
     switch (day)
     {
     case 0:
    	 System.out.println("this day is monday");
    	 break;
     case 1:
    	 System.out.println("this day is tuesday");
    	 break;
     case 2:
    	 System.out.println("this day is thursday");
    	 break;
     case 3:
    	 System.out.println("thsi day is friday");
    	 break;
     case 4:
    	 System.out.println("this day is sunday");
    	 break;
    	 default:
    	 System.out.println("the default is working");
    	 
     }
     
	}

}
