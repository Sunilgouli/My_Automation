package practice;

public class NestedIfElse {

	public static void main(String[] args) {
		int i=200;
		if (i==20)
			System.out.println("i is equal to 20");
		else if(i==40)
			System.out.println("i is equal to 40");
		else if (i==60)
			System.out.println("i is equal to 60");
		else if (i==80)
			System.out.println("i is equal to 80");
		else if (i==100)
			System.out.println("i is equal to 100");
		else
			System.out.println(" the else block is runned");

	}

}
